# Migrate

OpenPanel is fully OS-agnostic, running smoothly on any Linux distribution. This makes migrating the panel along with all user data from one server to another easy and efficient.

Use **OpenAdmin > Advanced > Migrate** to transfer all server data to a fresh OpenPanel installation on a new server.

> ⚠️ This feature is intended only for full server migrations.

For detailed instructions, see the [Migrate OpenAdmin to new server guide](https://openpanel.com/docs/articles/transfers/migrate-openadmin-to-new-server/)
